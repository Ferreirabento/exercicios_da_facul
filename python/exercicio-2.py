
A = 5
B = 3

#A soma
soma = A + B
print(soma)

#A subtração
menos = A - B
print(menos)

#A multiplicação
subtracao = A * B
print(subtracao)

#A divisão
divisao = A / B
print(divisao)

#O resto da divisão
resto_da_divisao = A % B
print(resto_da_divisao)

#A potência (ex: a**b)
potencia = A ** B
print(potencia)



